package basics;

//Basic for Hello World

/*
Java line by line execution.
 */

public class hello_world{
    public static void main(String args[]){
        System.out.println("Hello world");   // "ln" & "\n". are used for changing the line.
        System.out.print("Ashu Yadav\n");
        System.out.println("****");
        System.out.println("***");
        System.out.println("**");
        System.out.println("*");
        int a =100;
        System.out.println(a);
        String name ="Ashu Yadav";
        System.out.println(name);
        a =200;
        System.out.println(a);
    }
}